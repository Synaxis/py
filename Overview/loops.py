# Loops

#For Loop
people = ["John", "Sara", "Tim"]
for person in people:
   print("current person: ", person)

#Iteration by seq index
for i in range(len(people)):
   print("current person: ",people[i])

for i in rage(0, 10):
   print(i)

# While Loop
count = 0
while count < 10:
   print("count: ", count)
